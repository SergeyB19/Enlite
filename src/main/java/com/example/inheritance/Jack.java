package com.example.inheritance;

public class Jack extends Human{
    String name;
    String surname;
    int age;

    public Jack(String name, String surname, int age) {
        this.name = name;
        this.surname = surname;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getAge() {
        return age;
    }
}
